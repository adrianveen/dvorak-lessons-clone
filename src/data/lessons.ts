const abcdLessonMap = new Map<number, Lesson>(
  abcdLessons.map((lesson) => [lesson.id, lesson] as const),
);

const gatherExercises = (lessonIds: number[]) =>
  lessonIds.flatMap((lessonId) => abcdLessonMap.get(lessonId)?.exercises ?? []);

const collectKeys = (lessonIds: number[], baseKeyOrder: string[]) => {
  const seen = new Set<string>();
  const ordered: string[] = [];

  for (const key of baseKeyOrder) {
    if (!seen.has(key)) {
      seen.add(key);
      ordered.push(key);
    }
  }

  for (const lessonId of lessonIds) {
    const lesson = abcdLessonMap.get(lessonId);
    if (!lesson) continue;

    for (const key of lesson.keys) {
      if (!seen.has(key)) {
        seen.add(key);
        ordered.push(key);
      }
    }
  }

  return ordered;
};

